document.addEventListener('DOMContentLoaded', function () {
    const typewriter = document.querySelector('.typewriter-text');
    const texts = ['Funnel Building',
        'SEO Optimization',
        'Content Creation',
        'Digital Marketing']; // Replace with your desired texts
    let index = 0;
    let text = '';
    let isDeleting = false;

    function type() {
        const fullText = texts[index];
        if (isDeleting) {
            text = fullText.substring(0, text.length - 1);
        } else {
            text = fullText.substring(0, text.length + 1);
        }

        typewriter.textContent = text;

        let typingSpeed = isDeleting ? 50 : 150;

        if (!isDeleting && text === fullText) {
            typingSpeed = 2000; // Pause at the end of the text
            isDeleting = true;
            console.log("Deleting");
        } else if (isDeleting && text === '') {
            isDeleting = false;
            index = (index + 1) % texts.length;
            typingSpeed = 3000;
        }

        setTimeout(type, typingSpeed);
    }

    type();

    // Function to create the accordion HTML structure
    function generateAccordion(data) {
        const accordionContainer = document.getElementById('dynamicAccordion');
        let accordionHTML = '';

        Object.keys(data).forEach((majorTopic, index) => {
            accordionHTML += `
        <div class="accordion-item">
            <h2 class="accordion-header" id="heading${index}">
                <button class="accordion-button collapsed p-4" type="button" data-bs-toggle="collapse" data-bs-target="#collapse${index}" aria-expanded="false" aria-controls="collapse${index}">
                    ${majorTopic}
                </button>
            </h2>
            <div id="collapse${index}" class="accordion-collapse collapse" aria-labelledby="heading${index}" data-bs-parent="#dynamicAccordion">
                <div class="accordion-body">
                    ${generateInnerAccordion(data[majorTopic], index)}
                </div>
            </div>
        </div>`;
        });

        accordionContainer.innerHTML = `
    <div class="accordion" id="accordionLeft">
        ${accordionHTML}
    </div>`;
    }

    // Function to create inner accordion for subtopics
    function generateInnerAccordion(subtopics, majorIndex) {
        let innerHTML = '';

        subtopics.forEach((subtopic, subIndex) => {
            innerHTML += `
        <div class="accordion" id="accordionInner${majorIndex}-${subIndex}">
            <div class="accordion-item">
                <h2 class="accordion-header" id="heading${majorIndex}-${subIndex}">
                    <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseInner${majorIndex}-${subIndex}" aria-expanded="false" aria-controls="collapseInner${majorIndex}-${subIndex}">
                        ${subtopic.title}
                    </button>
                </h2>
                <div id="collapseInner${majorIndex}-${subIndex}" class="accordion-collapse collapse" aria-labelledby="heading${majorIndex}-${subIndex}" data-bs-parent="#accordionInner${majorIndex}-${subIndex}">
                    <div class="accordion-body">
                        <ul>
                            ${subtopic.content.map(item => `<li>${item}</li>`).join('')}
                        </ul>
                    </div>
                </div>
            </div>
        </div>`;
        });

        return innerHTML;
    }

    // Generate accordion
    generateAccordion(syllabus);

});

